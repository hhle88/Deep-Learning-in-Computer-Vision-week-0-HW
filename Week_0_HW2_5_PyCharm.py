# Problem:
# Write a Python program that will accept the base and height of a triangle and compute the area

# Solution:
print('Please insert base of a triangle:')
bas = int(input())
print('Please insert height of the triangle:')
hei = int(input())
area = bas*hei/2
print('The area of the triangle is:', area)
